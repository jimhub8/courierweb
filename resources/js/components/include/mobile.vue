<script>
import {
    vueTopprogress
} from "vue-top-progress";
import * as easings from "vuetify/es5/util/easing-patterns";
import Loader from "./Loader";
import Profile from './Profile'
export default {
    props: ['user'],
    components: {
        Loader,
        Profile
    },
    data() {
        return {
            search: "",
            loading: false,
            snackbar: false,
            timeout: 5000,
            Scolor: "",
            message: "Success",
            offsetTop: 0,
            duration: 1000,
            easing: "easeInOutCubic",
            easings: Object.keys(easings),
            service: 'none',
            why: 'none'
        };
    },
    methods: {
        progressBar() {
            this.$refs.topProgress.start();
        },

        onScroll(e) {
            this.offsetTop = window.pageYOffset || document.documentElement.scrollTop;
        },
        pageLoad() {
            this.loading = true;
            // var self = this
            setTimeout(() => {
                this.loading = false;
            }, 2000);
        },
        progress() {
            this.loading = true;
        },
        stopProgress() {
            this.loading = false;
        },
        redirect() {
            eventBus.$emit("searchEvent", this.search);
            this.$router.push({
                name: "search",
                params: {
                    search: this.search
                }
            });
        },

        showalert(data) {
            this.message = data;
            this.Scolor = "black";
            this.snackbar = true;
        },
        // getQuote () {
        //   this.offsetTop = 3437
        // },
        showerror(data) {
            this.message = data;
            this.Scolor = "red";
            this.snackbar = true;
        },
        serviceDisplay() {
            if (this.service === 'none') {
                this.service = 'block'
                this.why = 'none'
            } else {
                this.service = 'none'
                this.why = 'none'
            }
        },
        whyDisplay() {
            if (this.why === 'none') {
                this.service = 'none'
                this.why = 'block'
            } else {
                this.service = 'none'
                this.why = 'none'
            }
        }
    },
    mounted() {
        this.pageLoad();
    },
    created() {
        eventBus.$on("loaderEvent", data => {
            this.pageLoad();
            window.scrollTo(0, 0);
        });

        eventBus.$on("progressEvent", data => {
            this.progress();
            window.scrollTo(0, 0);
        });
        eventBus.$on("StopLoadingEvent", data => {
            this.stopProgress()
        });

        eventBus.$on("alertRequest", data => {
            this.showalert(data);
        });

        eventBus.$on("progressEvent", data => {
            this.$refs.topProgress.start();
        });
        eventBus.$on("errorEvent", data => {
            this.showerror(data);
        });

        // eventBus.$emit('scrollEvent')
        eventBus.$on("errorEvent", data => {
            this.showerror(data);
        });

        eventBus.$on("scrollEvent", data => {
            // window.scrollTo(0, 0);
            VueScroll.scrollTo("#headerq", 1000);
        });

        eventBus.$on("quoteEvent", data => {
            timeout = setTimeout(() => {
            window.scrollTo(0, 3650);
            }, 200);
            // window.scrollTo('#quatation', 1000);
            // VueScroll.scrollTo("#quote", 3437);
        });
    }
};
